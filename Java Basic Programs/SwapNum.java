class SwapNum
{
     public static void main(String[] args)
     {
          int no1 = 10 , no2 = 20 , no3;
         
          System.out.println("Number before swaping : "+no1+" "+no2);
          no3 = no1 ;
          no1 = no2;
          no2 = no3;
        System.out.println("Number after swaping : "+no1+" "+no2);
   
     }
}